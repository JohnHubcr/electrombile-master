public class CheckSubjectInfo {

    private String title;
    private int iconId;
    private Boolean hasTrouble;

    public Boolean getHasTrouble() {
        return hasTrouble;
    }

    public void setHasTrouble(Boolean hasTrouble) {
        this.hasTrouble = hasTrouble;
    }

    public CheckSubjectInfo() {
    }

    public CheckSubjectInfo(String title, int iconId, Boolean hasTrouble) {
        this.title = title;
        this.iconId = iconId;
        this.hasTrouble = hasTrouble;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getIconId() {
        return iconId;
    }

    public void setIconId(int iconId) {
        this.iconId = iconId;
    }

    @Override
    public String toString() {
        return "CheckSubjectInfo{" +
                "title='" + title + '\'' +
                ", iconId=" + iconId +
                ", hasTrouble=" + hasTrouble +
                '}';
    }
}
